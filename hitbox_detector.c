#include "game.h"

int	hitbox_detect(t_sprite *a, t_sprite *b)
{
	return (a->x < b->x + b->w
		&& a->x + a->w > b->x
		&& a->y < b->y + b->h
		&& a->y + a->h > b->y);
}
