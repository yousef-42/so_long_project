#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_node;
	void	*this_content;

	new_list = NULL;
	while (lst)
	{
		this_content = f(lst->content);
		new_node = ft_lstnew(this_content);
		if (!new_node)
		{
			del(this_content);
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_node);
		lst = lst->next;
	}
	return (new_list);
}
