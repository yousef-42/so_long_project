#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*lo;
	int		i;

	i = 0;
	lo = lst;
	while (lo != NULL)
	{
		i++;
		lo = lo->next;
	}
	return (i);
}
